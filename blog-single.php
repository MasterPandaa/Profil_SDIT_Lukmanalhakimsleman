                                                <p>Program tahfidz Al-Qur'an merupakan salah satu program unggulan di SDIT Luqman Al Hakim Sleman. Melalui program ini, siswa diharapkan mampu menghafal minimal 3 juz Al-Qur'an selama 6 tahun masa pendidikan.</p>
                                                <p>Kegiatan tahfidz Al-Qur'an di SDIT Luqman Al Hakim Sleman dilaksanakan setiap hari dengan alokasi waktu khusus. Kegiatan ini dibimbing oleh guru-guru yang memiliki kompetensi dalam bidang tahfidz Al-Qur'an. Selain itu, sekolah juga bekerja sama dengan lembaga tahfidz untuk memastikan kualitas program ini.</p>
                                                <blockquote>
                                                    <p>"Menghafal Al-Qur'an adalah salah satu cara terbaik untuk mendekatkan diri kepada Allah SWT dan menanamkan nilai-nilai Islam dalam diri anak sejak dini."</p>
                                                    <cite><a href="#">Ahmad Fauzi, S.Pd.I - Kepala Sekolah</a></cite>
                                                </blockquote>
                                                <p>Metode yang digunakan dalam program tahfidz Al-Qur'an di SDIT Luqman Al Hakim Sleman adalah metode talaqqi dan muraja'ah. Metode talaqqi adalah metode di mana guru membacakan ayat Al-Qur'an dan siswa menirukan. Sedangkan metode muraja'ah adalah metode mengulang hafalan yang sudah dimiliki agar tetap terjaga.</p>
                                                <p>Untuk mendukung program tahfidz Al-Qur'an, sekolah juga melibatkan orangtua dalam proses pemantauan hafalan siswa di rumah. Hal ini dilakukan melalui buku penghubung yang berisi catatan hafalan siswa dan harus ditandatangani oleh orangtua setiap hari.</p>
                                                <p>Selain itu, sekolah juga mengadakan kegiatan pendukung seperti lomba tahfidz Al-Qur'an, khataman Al-Qur'an, dan wisuda tahfidz yang dilaksanakan setiap tahun. Kegiatan-kegiatan ini bertujuan untuk memotivasi siswa dalam menghafal Al-Qur'an.</p>
                                                <p>Hasil dari program tahfidz Al-Qur'an di SDIT Luqman Al Hakim Sleman cukup membanggakan. Banyak siswa yang berhasil mencapai target hafalan bahkan melebihi target yang ditentukan. Beberapa siswa juga berhasil meraih prestasi dalam lomba tahfidz Al-Qur'an di tingkat kabupaten dan provinsi.</p>
                                                <p>Program tahfidz Al-Qur'an di SDIT Luqman Al Hakim Sleman tidak hanya bertujuan untuk menghafal Al-Qur'an, tetapi juga untuk memahami dan mengamalkan nilai-nilai yang terkandung di dalamnya. Dengan demikian, diharapkan siswa tidak hanya hafal Al-Qur'an secara tekstual, tetapi juga mampu mengimplementasikan nilai-nilai Al-Qur'an dalam kehidupan sehari-hari.</p>
